#!/usr/bin/env node
/**
 * generateBundle.mjs — Read all bank data from SQLite and write the 9 static
 * data files in src/data/ with identical export shapes to what existed before.
 *
 * This makes SQLite the single source of truth. The generated JS files are what
 * Vite bundles for the frontend — zero frontend changes needed.
 *
 * Run: npm run generate-bundle          (standalone)
 *      npm run seed-and-bundle          (seed then generate)
 *      npm run refresh-and-bundle       (pipeline then generate)
 */
import { getDb, closeDb, parseRow } from './db.mjs';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';
import { writeFileSync, readFileSync } from 'fs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const SRC_DATA = join(__dirname, '..', 'src', 'data');

const HEADER = '// AUTO-GENERATED by generateBundle.mjs — do not edit manually.\n// Run: npm run generate-bundle to regenerate from SQLite.\n';

// ── Helpers ──

function toJS(obj) {
  // JSON.stringify with 2-space indent, then strip quotes from keys where safe
  return JSON.stringify(obj, null, 2);
}

function writeDataFile(filename, content) {
  const path = join(SRC_DATA, filename);
  writeFileSync(path, HEADER + '\n' + content + '\n', 'utf8');
  return path;
}

// ── Main ──

const db = getDb();

// Safety check: abort if database is empty
const bankCount = db.prepare('SELECT COUNT(*) as c FROM banks').get().c;
if (bankCount === 0) {
  console.error('ERROR: No banks in database. Run seed.mjs first:');
  console.error('  npm run seed');
  process.exit(1);
}

console.log(`Generating bundle from SQLite (${bankCount} banks)...\n`);

let filesWritten = 0;

// ────────────────────────────────────────────────────────────────
// 1. banks.js — BANK_DATA
// ────────────────────────────────────────────────────────────────
{
  const rows = db.prepare('SELECT key, data FROM banks ORDER BY key').all();
  const obj = {};
  for (const row of rows) {
    obj[row.key] = JSON.parse(row.data);
  }
  writeDataFile('banks.js', `export const BANK_DATA = ${toJS(obj)};`);
  console.log(`  banks.js — ${rows.length} banks`);
  filesWritten++;
}

// ────────────────────────────────────────────────────────────────
// 2. qualification.js — QUAL_FRAMEWORK (static) + QUAL_DATA
// ────────────────────────────────────────────────────────────────
{
  // Preserve QUAL_FRAMEWORK verbatim from existing file
  const existingFile = readFileSync(join(SRC_DATA, 'qualification.js'), 'utf8');
  const frameworkMatch = existingFile.match(/export const QUAL_FRAMEWORK\s*=\s*\{[\s\S]*?\};\s*/);

  let frameworkBlock;
  if (frameworkMatch) {
    frameworkBlock = frameworkMatch[0].trim();
  } else {
    // Fallback: if we can't extract it (e.g., first run on freshly generated file), use hardcoded
    console.warn('  WARNING: Could not extract QUAL_FRAMEWORK from existing file. Using inline definition.');
    frameworkBlock = `export const QUAL_FRAMEWORK = {
  dimensions: {
    firmographics: { weight: 0.10, label: "Firmographics", desc: "Bank size, assets, tier, employees, financial health" },
    technographics: { weight: 0.15, label: "Technographics", desc: "Tech stack maturity, legacy burden, cloud adoption, API readiness" },
    decision_process: { weight: 0.10, label: "Decision Process", desc: "Clarity of buying process, budget cycle, governance speed" },
    landing_zones: { weight: 0.20, label: "Landing Zone Fit", desc: "How well Backbase products map to bank's actual needs" },
    pain_push: { weight: 0.20, label: "Pain & Push to Act", desc: "Urgency of pain points, regulatory pressure, competitive threat, transformation trigger" },
    power_map: { weight: 0.15, label: "Power Map", desc: "Do we know and have access to decision makers?" },
    partner_access: { weight: 0.10, label: "Partner Access", desc: "Does the bank work with partners we have relationships with?" }
  },
  // power_map_activated: boolean — if YES, +1.5 bonus; if NO, -1 penalty
  // partner_known: boolean — if YES, +0.5 bonus
};`;
  }

  const rows = db.prepare('SELECT bank_key, data FROM qualification ORDER BY bank_key').all();
  const obj = {};
  for (const row of rows) {
    obj[row.bank_key] = JSON.parse(row.data);
  }

  const content = `${frameworkBlock}\n\n\nexport const QUAL_DATA = ${toJS(obj)};`;
  writeDataFile('qualification.js', content);
  console.log(`  qualification.js — QUAL_FRAMEWORK (preserved) + ${rows.length} banks`);
  filesWritten++;
}

// ────────────────────────────────────────────────────────────────
// 3. cx.js — CX_DATA
// ────────────────────────────────────────────────────────────────
{
  const rows = db.prepare('SELECT bank_key, data FROM cx ORDER BY bank_key').all();
  const obj = {};
  for (const row of rows) {
    obj[row.bank_key] = JSON.parse(row.data);
  }
  writeDataFile('cx.js', `export const CX_DATA = ${toJS(obj)};`);
  console.log(`  cx.js — ${rows.length} banks`);
  filesWritten++;
}

// ────────────────────────────────────────────────────────────────
// 4. competition.js — COMP_DATA
// ────────────────────────────────────────────────────────────────
{
  const rows = db.prepare('SELECT bank_key, data FROM competition ORDER BY bank_key').all();
  const obj = {};
  for (const row of rows) {
    obj[row.bank_key] = JSON.parse(row.data);
  }
  writeDataFile('competition.js', `export const COMP_DATA = ${toJS(obj)};`);
  console.log(`  competition.js — ${rows.length} banks`);
  filesWritten++;
}

// ────────────────────────────────────────────────────────────────
// 5. valueSelling.js — VALUE_SELLING
// ────────────────────────────────────────────────────────────────
{
  const rows = db.prepare('SELECT bank_key, data FROM value_selling ORDER BY bank_key').all();
  const obj = {};
  for (const row of rows) {
    obj[row.bank_key] = JSON.parse(row.data);
  }
  writeDataFile('valueSelling.js', `export const VALUE_SELLING = ${toJS(obj)};`);
  console.log(`  valueSelling.js — ${rows.length} banks`);
  filesWritten++;
}

// ────────────────────────────────────────────────────────────────
// 6. relationships.js — GROUP_RELATIONSHIPS
// ────────────────────────────────────────────────────────────────
{
  const rows = db.prepare('SELECT bank_key, data FROM relationships ORDER BY bank_key').all();
  const obj = {};
  for (const row of rows) {
    obj[row.bank_key] = JSON.parse(row.data);
  }
  writeDataFile('relationships.js', `export const GROUP_RELATIONSHIPS = ${toJS(obj)};`);
  console.log(`  relationships.js — ${rows.length} relationships`);
  filesWritten++;
}

// ────────────────────────────────────────────────────────────────
// 7. sources.js — SOURCES (grouped rows, not blobs)
// ────────────────────────────────────────────────────────────────
{
  const rows = db.prepare('SELECT ref_key, label, url, category FROM sources ORDER BY ref_key, id').all();
  const obj = {};
  for (const row of rows) {
    if (!obj[row.ref_key]) obj[row.ref_key] = [];
    const entry = { label: row.label };
    if (row.url) entry.url = row.url;
    if (row.category) entry.cat = row.category;
    obj[row.ref_key].push(entry);
  }
  writeDataFile('sources.js', `export const SOURCES = ${toJS(obj)};`);
  console.log(`  sources.js — ${rows.length} source entries across ${Object.keys(obj).length} ref keys`);
  filesWritten++;
}

// ────────────────────────────────────────────────────────────────
// 8. markets.js — MARKETS_META + MARKET_DATA
// ────────────────────────────────────────────────────────────────
{
  const rows = db.prepare('SELECT key, name, countries, emoji, has_data, data FROM markets ORDER BY key').all();
  const meta = {};
  const data = {};
  for (const row of rows) {
    meta[row.key] = {
      name: row.name,
      countries: JSON.parse(row.countries),
      emoji: row.emoji || undefined,
      hasData: row.has_data === 1,
    };
    if (row.data) {
      data[row.key] = JSON.parse(row.data);
    }
  }

  // Clean undefined values for cleaner output
  for (const m of Object.values(meta)) {
    if (m.emoji === undefined) delete m.emoji;
  }

  const content = `export const MARKETS_META = ${toJS(meta)};\n\nexport const MARKET_DATA = ${toJS(data)};`;
  writeDataFile('markets.js', content);
  console.log(`  markets.js — ${rows.length} markets (${Object.keys(data).length} with data)`);
  filesWritten++;
}

// ────────────────────────────────────────────────────────────────
// 9. countries.js — COUNTRY_DATA
// ────────────────────────────────────────────────────────────────
{
  const rows = db.prepare('SELECT name, data FROM countries ORDER BY name').all();
  const obj = {};
  for (const row of rows) {
    obj[row.name] = JSON.parse(row.data);
  }
  writeDataFile('countries.js', `export const COUNTRY_DATA = ${toJS(obj)};`);
  console.log(`  countries.js — ${rows.length} countries`);
  filesWritten++;
}

// ── Done ──

closeDb();
console.log(`\nDone! ${filesWritten} files generated in src/data/`);
console.log('SQLite is now the single source of truth.');
